const today = new Date();
const fmt = (d) => d.toISOString().split("T")[0];

const genEntry = (farmerId, daysAgo, shift, ltrs, fat, snf, rate, paid) => {
  const d = new Date(today);
  d.setDate(d.getDate() - daysAgo);
  const amount = parseFloat((ltrs * rate).toFixed(2));
  return {
    id: `E${farmerId}${daysAgo}${shift}`,
    farmerId,
    date: fmt(d),
    shift,
    kgs: ltrs,
    ltrs,
    fat,
    snf,
    rate,
    amount,
    paid,
  };
};

